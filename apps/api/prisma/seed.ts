async function main() {
  const isProduction = process.env.NODE_ENV === 'production';
  const generatedAdminPassword = randomBytes(18).toString('base64url');
  const generatedStudentPassword = randomBytes(18).toString('base64url');
  const adminEmail = process.env.SEED_ADMIN_EMAIL ?? (isProduction ? undefined : 'admin@codequest.dev');
  const adminPlainPassword = process.env.SEED_ADMIN_PASSWORD ?? (isProduction ? undefined : generatedAdminPassword);
  const studentEmail = process.env.SEED_STUDENT_EMAIL ?? (isProduction ? undefined : 'student@codequest.dev');
  const studentPlainPassword = process.env.SEED_STUDENT_PASSWORD ?? (isProduction ? undefined : generatedStudentPassword);

  if (!adminEmail || !adminPlainPassword || !studentEmail || !studentPlainPassword) {
    throw new Error('Production seed requires SEED_ADMIN_EMAIL, SEED_ADMIN_PASSWORD, SEED_STUDENT_EMAIL and SEED_STUDENT_PASSWORD.');
  }

  const adminPassword = await bcrypt.hash(adminPlainPassword, 12);
  const userPassword = await bcrypt.hash(studentPlainPassword, 12);

  const [starterTitle] = await Promise.all([
    prisma.title.upsert({
      where: { key: 'promising_beginner' },
      update: {},
      create: { key: 'promising_beginner', name: 'Iniciante Promissor', description: 'Primeiros passos consistentes.' },
    }),
    ...[
      ['bug_hunter', 'Caçador de Bugs'],
      ['react_specialist', 'Especialista em React'],
      ['typescript_master', 'Mestre do TypeScript'],
      ['clean_code_guardian', 'Guardião do Código Limpo'],
      ['arena_legend', 'Lenda da Arena'],
      ['technical_curator', 'Curador Técnico'],
      ['frontend_architect', 'Arquiteto Front-End'],
      ['sql_strategist', 'Estrategista SQL'],
    ].map(([key, name]) =>
      prisma.title.upsert({
        where: { key },
        update: {},
        create: { key, name, description: `Título desbloqueável: ${name}.` },
      }),
    ),
  ]);

  const admin = await prisma.user.upsert({
    where: { email: adminEmail },
    update: {},
    create: {
      name: 'Admin CodeQuest',
      email: adminEmail,
      passwordHash: adminPassword,
      role: 'ADMIN',
      activeTitleId: starterTitle.id,
      xp: 2840,
      rating: 1280,
    },
  });

  const user = await prisma.user.upsert({
    where: { email: studentEmail },
    update: {},
    create: {
      name: 'Leo Student',
      email: studentEmail,
      passwordHash: userPassword,
      activeTitleId: starterTitle.id,
      xp: 640,
      rating: 1020,
    },
  });

  await prisma.userTitle.upsert({
    where: { userId_titleId: { userId: user.id, titleId: starterTitle.id } },
    update: {},
    create: { userId: user.id, titleId: starterTitle.id },
  });

  const categoryMap = new Map<string, string>();

  for (const [name, slug] of categories) {
    const category = await prisma.category.upsert({
      where: { slug },
      update: {
        name,
        description: `Perguntas de ${name}`,
      },
      create: {
        name,
        slug,
        description: `Perguntas de ${name}`,
      },
    });

    categoryMap.set(slug, category.id);
  }

  for (const item of prompts) {
    const categoryId = categoryMap.get(item.category);

    if (!categoryId) {
      console.warn(`Categoria não encontrada para a pergunta: ${item.prompt}`);
      continue;
    }

    const alternatives = [
      { text: item.correct, isCorrect: true },
      ...item.wrong.map((text) => ({ text, isCorrect: false })),
    ];

    const existingQuestion = await prisma.question.findFirst({
      where: { prompt: item.prompt },
      select: { id: true },
    });

    if (existingQuestion) {
      await prisma.question.update({
        where: { id: existingQuestion.id },
        data: {
          categoryId,
          authorId: admin.id,
          prompt: item.prompt,
          explanation: `Resposta correta: ${item.correct}.`,
          difficulty: item.difficulty,
          status: 'APPROVED',
          isActive: true,
          reviewedById: admin.id,
          reviewedAt: new Date(),
          alternatives: {
            deleteMany: {},
            create: alternatives,
          },
        },
      });
    } else {
      await prisma.question.create({
        data: {
          categoryId,
          authorId: admin.id,
          prompt: item.prompt,
          explanation: `Resposta correta: ${item.correct}.`,
          difficulty: item.difficulty,
          status: 'APPROVED',
          isActive: true,
          reviewedById: admin.id,
          reviewedAt: new Date(),
          alternatives: {
            create: alternatives,
          },
        },
      });
    }
  }

  await prisma.question.updateMany({
    where: { status: 'APPROVED' },
    data: {
      isActive: true,
      reviewedById: admin.id,
      reviewedAt: new Date(),
    },
  });

  const achievements = [
    ['first_quiz', 'Primeiro quiz', 'Complete seu primeiro quiz.', 25],
    ['ten_quizzes', '10 quizzes concluídos', 'Complete 10 quizzes.', 100],
    ['hundred_correct', '100 respostas corretas', 'Acerte 100 perguntas.', 150],
    ['perfect_quiz', 'Primeiro quiz perfeito', 'Finalize um quiz sem erros.', 50],
    ['react_i', 'React I', 'Acerte 25 perguntas de React.', 40],
    ['top_10', 'Top 10 Ranking', 'Entre no ranking geral.', 75],
    ['arena_master', 'Mestre da Arena', 'Vença desafios ranqueados.', 120],
  ] as const;

  for (const [key, name, description, xpBonus] of achievements) {
    await prisma.achievement.upsert({
      where: { key },
      update: {},
      create: { key, name, description, xpBonus, category: 'general' },
    });
  }

  const missions = [
    ['daily_10_questions', 'DAILY', 'Responder 10 perguntas', 10, 80],
    ['daily_js_5', 'DAILY', 'Acertar 5 perguntas de JavaScript', 5, 60],
    ['weekly_3_quizzes', 'WEEKLY', 'Completar 3 quizzes', 3, 180],
    ['weekly_arena_win', 'WEEKLY', 'Vencer 1 desafio na Arena', 1, 220],
  ] as const;

  for (const [key, type, title, target, xpReward] of missions) {
    await prisma.mission.upsert({
      where: { key },
      update: {},
      create: { key, type, title, target, xpReward },
    });
  }

  const tracks = [
    ['Trilha Front-End', 'HTML -> CSS -> JavaScript -> TypeScript -> React', ['html', 'css', 'javascript', 'typescript', 'react']],
    ['Trilha Back-End', 'Node.js -> APIs REST -> SQL -> Segurança', ['node', 'apis-rest', 'sql', 'database', 'security']],
    ['Trilha Entrevista Técnica', 'Lógica -> Algoritmos -> JavaScript -> React -> System Design básico', ['algorithms', 'data-structures', 'javascript', 'react', 'architecture']],
  ] as const;

  for (const [name, description, slugs] of tracks) {
    const track = await prisma.skillTrack.upsert({
      where: { name },
      update: {
        description,
        finalBadge: `${name} Master`,
      },
      create: {
        name,
        description,
        finalBadge: `${name} Master`,
      },
    });

    let order = 1;

    for (const slug of slugs) {
      const categoryId = categoryMap.get(slug);

      if (categoryId) {
        await prisma.skillTrackCategory.upsert({
          where: { trackId_categoryId: { trackId: track.id, categoryId } },
          update: { order },
          create: { trackId: track.id, categoryId, order },
        });
      }

      order += 1;
    }
  }

  const existingSeason = await prisma.season.findFirst({
    where: { name: 'Temporada Junho 2026' },
  });

  if (!existingSeason) {
    await prisma.season.create({
      data: {
        name: 'Temporada Junho 2026',
        startsAt: new Date('2026-06-01T00:00:00.000Z'),
        endsAt: new Date('2026-06-30T23:59:59.000Z'),
        rewards: { top1: 'Lenda da Arena', top10: 'Top 10 Ranking' },
      },
    });
  }

  console.log(`Seed completed. ${prompts.length} perguntas inseridas/atualizadas.`);
  console.log(`Admin seed user: ${adminEmail}`);
  console.log(`Student seed user: ${studentEmail}`);
  if (!isProduction) {
    console.log(`Admin seed password: ${adminPlainPassword}`);
    console.log(`Student seed password: ${studentPlainPassword}`);
  }
}

main()
  .catch((error) => {
    console.error(error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
